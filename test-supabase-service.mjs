const serviceKey = '***REMOVED***'

const supabase = createClient(url, serviceKey)

console.log('Creating test table and inserting data...\n')

// Create test_records table using raw SQL
const { data: createResult, error: createError } = await supabase.rpc('exec_sql', {
  sql: `
    CREATE TABLE IF NOT EXISTS test_records (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      message TEXT,
      created_at TIMESTAMPTZ DEFAULT NOW()
    );

    ALTER TABLE test_records ENABLE ROW LEVEL SECURITY;

    DROP POLICY IF EXISTS "Public select on test_records" ON test_records;
    DROP POLICY IF EXISTS "Public insert on test_records" ON test_records;

    CREATE POLICY "Public select on test_records"
      ON test_records FOR SELECT USING (true);

    CREATE POLICY "Public insert on test_records"
      ON test_records FOR INSERT WITH CHECK (true);
  `
})

if (createError) {
  console.log('RPC exec_sql not available, trying direct approach...')
}

// Try direct insert (will work if table exists or after creation via dashboard)
